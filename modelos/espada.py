class Espada:
    def __init__(self, nombre, daño, modelo, precio):
        self.nombre = nombre
        self.daño = daño
        self.modelo = modelo 
        self.precio = precio

        def __str__(self):
            return f"Espada: {self.nombre}, Daño: {self.daño}, Modelo: {self.modelo}, Precio: {self.precio}"
        
    
    

   
     